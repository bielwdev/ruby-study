# In Ruby, `puts` and `print` are both used to display output to the console,
# but they have some key differences.

# `puts` adds a new line after the output
puts "Hello, world!"
puts "This is Ruby"

# Output:
# Hello, world!
# This is Ruby
# Notice that each string is printed on a new line.

# `print` does not add a new line after the output
print "Hello, world!"
print "This is Ruby"

# Output:
# Hello, world!This is Ruby
# Notice that the strings are printed on the same line.
